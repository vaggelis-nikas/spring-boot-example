package com.intrasoft.handson.exception;

public class IntlDataException extends IntlException {

	public IntlDataException() {

		super();
	}

	public IntlDataException(final String message) {

		super(message);
	}

	public IntlDataException(final Throwable cause) {

		super(cause);
	}

	public IntlDataException(final String message, final Throwable cause) {

		super(message, cause);
	}

}
