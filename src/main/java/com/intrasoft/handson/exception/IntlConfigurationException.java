package com.intrasoft.handson.exception;

public class IntlConfigurationException extends IntlException {

	public IntlConfigurationException() {

		super();
	}

	public IntlConfigurationException(final String message) {

		super(message);
	}

	public IntlConfigurationException(final Throwable cause) {

		super(cause);
	}

	public IntlConfigurationException(final String message, final Throwable cause) {

		super(message, cause);
	}

}
