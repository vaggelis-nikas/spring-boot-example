package com.intrasoft.handson.exception;

public class IntlValidationException extends IntlException {

	public IntlValidationException() {

		super();
	}

	public IntlValidationException(final String message) {

		super(message);
	}

	public IntlValidationException(final Throwable cause) {

		super(cause);
	}

	public IntlValidationException(final String message, final Throwable cause) {

		super(message, cause);
	}

}
