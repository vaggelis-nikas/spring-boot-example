package com.intrasoft.handson.exception;

public class IntlConcurrencyException extends IntlException {

	public IntlConcurrencyException() {

		super();
	}

	public IntlConcurrencyException(final String message) {

		super(message);
	}

	public IntlConcurrencyException(final Throwable cause) {

		super(cause);
	}

	public IntlConcurrencyException(final String message, final Throwable cause) {

		super(message, cause);
	}

}
