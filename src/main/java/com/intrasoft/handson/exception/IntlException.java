package com.intrasoft.handson.exception;

public class IntlException extends Exception {

	public IntlException() {

		super();
	}

	public IntlException(final String message) {

		super(message);
	}

	public IntlException(final Throwable cause) {

		super(cause);
	}

	public IntlException(final String message, final Throwable cause) {

		super(message, cause);
	}
}
